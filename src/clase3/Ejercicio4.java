    private void OPERARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OPERARActionPerformed
        /*
        Ejercicio 4: Sumar números hasta un límite
        Pide al usuario ingresar números y súmalos hasta que el total sea mayor o igual a 100.
         */
        double suma = 0;
        while (suma < 100) {
            int n1 = Integer.parseInt(JOptionPane.showInputDialog("Ingrese Numero"));
            suma += n1;
        }
        if (suma == 100) {
            MOSTRAR.setText("LA SUMA LLEGO A 100");
        } else {
            MOSTRAR.setText("LA SUMA SUPERA A 100: " + suma);

        }
    }//GEN-LAST:event_OPERARActionPerformed

