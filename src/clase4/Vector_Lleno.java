    private void AgregarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarBActionPerformed
        
        if (contador<nombres.length) {
            nombres[contador] = Nombres.getText();
            contador++;
            Nombres.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "< VECTOR LLENO >");
        }
    }//GEN-LAST:event_AgregarBActionPerformed

    private void MostrarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarBActionPerformed
        MostrarA.setText("");
        MostrarA.append("Nombres ingresados: " + nombres.length);
        for (int i = 0; i < nombres.length; i++) {
            MostrarA.append("\nNombre" + (i + 1) + ": " + nombres[i]);
        }
    }//GEN-LAST:event_MostrarBActionPerformed

